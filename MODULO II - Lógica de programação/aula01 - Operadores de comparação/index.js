
// OPERADORES DE COMPARAÇÃO (BOOLEAN - Verdadeiro ou Falso)

//    >   - Maior que
            let maior = 10 > 5;

//    >=  - Maior ou igual a
            let maiorIgual = 10 >= 5;

//    <   - Menor
            let menor = 10 < 5;

//    <=  - Menor ou igual a
            let menorIgual = 10 <= 5;

//    ==  - Igual -> NÃO É RECOMENDADO SEU USO
            let igual = 10 == 10;

//    === - Igual estrito / Identico
            let identico = 10 === 10;

//    !=  - Diferente
            let diferente = 10 != 10;

//    !== - Diferente estrito
            let diferenteEstrito = 10 !== 10;


